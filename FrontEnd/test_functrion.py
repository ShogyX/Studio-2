def test_for_leet(input_string):
    new_word = input_string
    for letter in leetspeak_dict.keys():
        list_of_subs = leetspeak_dict[letter]
        for sub in list_of_subs:
            if sub in input_string:
                new_word = new_word.replace(sub, letter)
    
    return new_word

